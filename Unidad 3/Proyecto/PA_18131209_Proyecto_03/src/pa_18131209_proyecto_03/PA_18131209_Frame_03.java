    private void jButGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGrabarActionPerformed
        // TODO add your handling code here:
        
        FilmeA1 pel1 = new FilmeA1( 
                      (String)modelo.getValueAt(0, 0),
                      (int)modelo.getValueAt(0, 1),
                      (String)modelo.getValueAt(0, 2),
                      "Deadpool", "Estados Unidos");
            
        FilmeA2 pel2 = new FilmeA2( 
                      (String)modelo.getValueAt(1, 0),
                      (int)modelo.getValueAt(1, 1),
                      (String)modelo.getValueAt(1, 2),
                      "Linterna Verde", "DC");
                                   
        FilmeT1 pel3 = new FilmeT1( 
                      (String)modelo.getValueAt(2, 0),
                      (int)modelo.getValueAt(2, 1),
                      (String)modelo.getValueAt(2, 2),
                      "REC", "Filmax");
                                   
        FilmeT2 pel4 = new FilmeT2( 
                      (String)modelo.getValueAt(3, 0),
                      (int)modelo.getValueAt(3, 1),
                      (String)modelo.getValueAt(3, 2),
                      "Pennywise", 700381748);
                
        filmes[0] = pel1;
        filmes[1] = pel2;
        filmes[2] = pel3;
        filmes[3] = pel4;

        for(int  i = 0; i <filmes.length-1; i++)
        {
            for(int j = i+1; j<filmes.length; j++)
            {
                if(filmes[i].getNombre().length() < filmes[j].getNombre().length())
                {
                    Pelicula aux = filmes[i];
                    filmes[i] = filmes[j];
                    filmes[j] = aux;
                }
            }
        }
        
        this.hablitar();
        
    }//GEN-LAST:event_jButGrabarActionPerformed

