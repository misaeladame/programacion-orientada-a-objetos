    private void jButMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMostrarActionPerformed
        // TODO add your handling code here:
        
        switch(Integer.parseInt(jSpinnerPosicion.getValue()+""))
        {
            case 1:
                filmes[0].dibujar(0, jPanelDibujo);
                jTextAreaInfo.setText(filmes[0].mostrar());
                jSpinnerPosicion.setValue(2);
                break;
                
            case 2:
                filmes[1].dibujar(0, jPanelDibujo);
                jTextAreaInfo.setText(filmes[1].mostrar());
                jSpinnerPosicion.setValue(3);
                break;
                
            case 3:
                filmes[2].dibujar(0, jPanelDibujo);
                jTextAreaInfo.setText(filmes[2].mostrar());
                jSpinnerPosicion.setValue(4);
                break;
                
            case 4:
                filmes[3].dibujar(0, jPanelDibujo);
                jTextAreaInfo.setText(filmes[3].mostrar());
                jSpinnerPosicion.setValue(1);
                break;
                
            default:
                break;
        }

    }//GEN-LAST:event_jButMostrarActionPerformed

