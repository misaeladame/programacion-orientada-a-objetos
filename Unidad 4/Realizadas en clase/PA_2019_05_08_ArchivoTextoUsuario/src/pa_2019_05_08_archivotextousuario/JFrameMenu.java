    private void jMenuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuGuardarActionPerformed
        if( jfch.showSaveDialog(jLabel1) != 0 )
            return;
            FileWriter fw = null;
            PrintWriter pw = null;
        try {
            fw = new FileWriter(jfch.getSelectedFile());
            pw = new PrintWriter(fw);
            pw.println(modelo.getRowCount());
            for (int r = 0; r < modelo.getRowCount(); r++) 
                for (int c = 0; c < modelo.getColumnCount()-1; c++) {
                        pw.println(modelo.getValueAt(r, c));
                }
            pw.close();
            fw.close();
                
            
            } catch (IOException ex) {
            Logger.getLogger(JFrameMenu.class.getName()).log(Level.SEVERE, null, ex);
        } 
        finally{
            if(pw != null)
                 pw.close();
            try {
                if(fw != null)
                fw.close();
    
            } catch (IOException ex) {
                Logger.getLogger(JFrameMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuGuardarActionPerformed

    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
        if( jfch.showOpenDialog(jLabel1) != 0)
            return;
        
            FileReader fr = null;
            BufferedReader br = null;        
        try {
            fr = new FileReader(jfch.getSelectedFile());
            br = new BufferedReader(fr);
           
            int n = Integer.parseInt( br.readLine() );
            jSpinAlumnos.setValue(n);
            
            for (int r = 0; r < n; r++) {
                for (int c = 0; c < modelo.getColumnCount()-1; c++) {
                    modelo.setValueAt(br.readLine(), r, c);
                }
            }
            jButCalcular.doClick();
            br.close();
            fr.close();
       //Atrapar excepciones    
        }catch (NumberFormatException ex){
            JOptionPane.showMessageDialog(rootPane,"Dato incorrecto "+ex.getMessage());
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Error de archivo"+ex.getMessage());
        }  
        catch (NullPointerException ex) {
            JOptionPane.showMessageDialog(rootPane, "Objeto nulo, no existe"+ex.getMessage());
        }   
        catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "No se que pasó, pero algo pasó"+ex.getMessage());
        }   
        finally{
            try {
                if(br != null)
                  br.close();
                if(fr != null)
                  fr.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrameMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jMenuAbrirActionPerformed

