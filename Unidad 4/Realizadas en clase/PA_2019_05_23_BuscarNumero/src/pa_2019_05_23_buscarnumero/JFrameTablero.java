    private void jMenuAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuAbrirActionPerformed
        // TODO add your handling code here:
        if(jfch.showOpenDialog(jLabel1) != 0)
            return;
        
        FileInputStream fis = null;
        DataInputStream dis = null;
        
        try {
            fis = new FileInputStream(jfch.getSelectedFile());
            dis = new DataInputStream(fis);
            
            numeroABuscar = dis.readInt();
            intentos = dis.readInt();
            int colorBoton = dis.readInt();
            
            jLabNumero.setText(numeroABuscar+"");
            jLabIntentos.setText(intentos+"");
            
            for(int i=0; i<tablero.length; i++)
            {
                tablero[i].setNumero(dis.readInt());
                tablero[i].setEnabled(dis.readBoolean());
                tablero[i].setBackground(new Color(colorBoton));
                
                if(tablero[i].isEnabled())
                {
                    tablero[i].setText("");
                }
                else
                {
                    tablero[i].mostrar();
                }
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            if(dis != null)
                try {
                    dis.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (fis != null)
                try {
                    fis.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jMenuAbrirActionPerformed

    private void jMenuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuGuardarActionPerformed
        // TODO add your handling code here:
        if(jfch.showSaveDialog(jLabel1) != 0)
            return;
        
        FileOutputStream fos = null;
        DataOutputStream dos = null;
        
        try {
            fos = new FileOutputStream(jfch.getSelectedFile());
            dos = new DataOutputStream(fos);
            
            dos.writeInt(numeroABuscar);
            dos.writeInt(intentos);
            dos.writeInt(tablero[0].getBackground().getRGB());
            
            for(MiBoton mb:tablero)
            {
                dos.writeInt(mb.getNumero());
                dos.writeBoolean(mb.isEnabled());
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            try {
                if(dos != null)
                    dos.close();
                if(fos != null)
                    fos.close();
   } catch (IOException ex) {
                Logger.getLogger(JFrameTablero.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jMenuGuardarActionPerformed

