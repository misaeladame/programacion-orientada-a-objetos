    @Override
    public void actionPerformed(ActionEvent e) {
        MiBoton boton = (MiBoton)e.getSource();
        
        boton.mostrar();
        boton.setEnabled(false);
        
        intentos++;
        jLabIntentos.setText(intentos+"");
        
        if(boton.getNumero() == numeroABuscar)
        {
            JOptionPane.showMessageDialog(rootPane, "Ya le atinÃ³!!");
            
            for(MiBoton mb:tablero)
                mb.mostrar();
        }
    }

