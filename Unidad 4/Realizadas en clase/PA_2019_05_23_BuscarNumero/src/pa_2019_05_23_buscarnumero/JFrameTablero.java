    public void iniciar()
    {
        int x = 30;
        int y = 50;
        
        MiBoton.setColor(2);
        
        for(int i=0; i<tablero.length; i++)
        {
            tablero[i] = new MiBoton(azar.nextInt(100));
            tablero[i].setLocation(x, y);

            x+=100;
            
            if((i+1)%5 ==0)
            {
                x = 30;
                y+=100;
            }
            
            tablero[i].addActionListener(this);
            
            add(tablero[i]);
        }
        
        intentos = 0;
        jLabIntentos.setText(intentos+"");
        
        numeroABuscar = tablero[azar.nextInt(tablero.length)].getNumero();
        jLabNumero.setText(numeroABuscar+"");
    }

