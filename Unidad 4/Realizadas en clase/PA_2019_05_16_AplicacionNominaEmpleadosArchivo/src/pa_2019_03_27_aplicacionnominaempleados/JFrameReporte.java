    private void jButExcelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButExcelActionPerformed
        // TODO add your handling code here:
        JFileChooser jfch = new JFileChooser();
        
        if (jfch.showSaveDialog(jfMenu) !=0)
            return;
        
        FileWriter fw = null;
        PrintWriter pw = null;
        
        try {
            fw = new FileWriter(jfch.getSelectedFile());
            pw = new PrintWriter(fw);
            

            pw.println("Número,Empleado,Piezas,Precio Pza,Ventas,Comisión,Días Trab,Sueldo diario,Bono,Sueldo");
            
            Empleado emp;
            String clase;
            
            for(int i=0; i<jfMenu.getEmpleadosRegistrados(); i++)
            {
                emp = jfMenu.getEmpleado(i);
                clase = emp.getClass().getSimpleName();
                
                if(clase.equals("Empleado"))
                    pw.println(emp.getNumero()+","+emp.getNombre());

                if(clase.equals("Taller"))
                {
                    Taller t = (Taller)emp;
                    
                    pw.println(emp.getNumero()+","+emp.getNombre()+","+t.getPiezas()+","+t.getPrecioUnitario());
                }
                
                if(clase.equals("Ventas"))
                {
                    Ventas v = (Ventas)emp;
                    
                    pw.println(emp.getNumero()+","+emp.getNombre()+",,,"+v.getVentasTotales()+","+v.getComision());
                }

                if(clase.equals("Administrativo"))
                {
                    Administrativo a = (Administrativo)emp;
                    
                    pw.println(emp.getNumero()+","+emp.getNombre()+",,,,,"+a.getDiasTrabajados()+","+a.getSueldoDiario());
                }

                if(clase.equals("Ejecutivo"))
                {
                    Ejecutivo e = (Ejecutivo)emp;
                    
                    pw.println(emp.getNumero()+","+emp.getNombre()+",,,,,"+e.getDiasTrabajados()+","+e.getSueldoDiario()+","+e.getBono());
                }
            }
            
        } catch (IOException ex) {
            Logger.getLogger(JFrameRegistro.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            if(pw != null)
                pw.close();
            
            if(fw != null)
                try {
                    fw.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrameRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jButExcelActionPerformed

    private void jButReportePDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButReportePDFActionPerformed
        // TODO add your handling code here:
        JFileChooser jfch = new JFileChooser();
        
        if (jfch.showSaveDialog(jfMenu) !=0)
            return;
        
        FileOutputStream fos = null;
        Document doc = null;
        
        try {
            fos = new FileOutputStream(jfch.getSelectedFile());
            doc = new Document();
            
            PdfWriter.getInstance(doc, fos);
            doc.open();
            
            doc.add(new Paragraph("Lista de Nómina",FontFactory.getFont("arial",   // fuente
                                                    22,                            // tamaño
                                                    Font.ITALIC,                   // estilo
                                                    BaseColor.GREEN)));

            doc.add(new Paragraph(" "));
            
            ordenarAlfabetico();
            
            Empleado emp;
            String espacios = "                                                ";
            String nombre, sueldo;
            DecimalFormat df = new DecimalFormat("$#,###,##0.00");
            
            Font fuenteAzul = FontFactory.getFont("courier",16,Font.NORMAL,BaseColor.BLUE);
            Font fuenteRojo = FontFactory.getFont("courier",16,Font.NORMAL,BaseColor.RED);
            
            for(int i=0; i<copia.length; i++)
            {
                emp = copia[i];
                nombre = (emp.getNombre()+espacios).substring(0, 35);
                sueldo = espacios + df.format(emp.sueldo());
                sueldo = sueldo.substring(sueldo.length()-15);
                doc.add(new Paragraph(nombre+sueldo,emp.sueldo()>0?fuenteAzul:fuenteRojo));
            }

            doc.add(new Paragraph(" "));

            Image foto = Image.getInstance("diver28.jpg");
            foto.scaleToFit(100, 100);
            foto.setAlignment(1); // centro
            doc.add(foto);

            doc.add(new Paragraph(" "));
            
            PdfPTable tabla = new PdfPTable(modelo.getColumnCount());
            
            int [] anchoColumnas = new int[modelo.getColumnCount()];
            
            int ancho;
            for(int i=0; i<anchoColumnas.length; i++)
            {
                switch(i)
                {
                    case 0: ancho = 40; break; // número de empleado
                    case 1: ancho = 200; break; // nombre
                    case 2: ancho = 50; break; // piezas
                    case 6: ancho = 50; break; // días trabajados
                    default: ancho = 80;
                }
                
                anchoColumnas[i]= ancho;
            }
            
            tabla.setWidths(anchoColumnas);

            Font fuenteTabla = FontFactory.getFont("courier",8,Font.NORMAL,BaseColor.BLACK);
            Font fuenteEnc = FontFactory.getFont("arial",4,Font.BOLD,BaseColor.RED);
            
            for(int c=0; c<modelo.getColumnCount(); c++)
                tabla.addCell(new Paragraph(jTableEmpleados.getColumnModel().getColumn(c).getHeaderValue()+"",fuenteEnc));
            
            for(int r=0; r<modelo.getRowCount(); r++)
                for(int c=0; c<modelo.getColumnCount(); c++)
                    tabla.addCell(new Paragraph(modelo.getValueAt(r, c)==null?"":modelo.getValueAt(r, c)+"",fuenteTabla));
            
            doc.add(tabla);
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(JFrameReporte.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(JFrameReporte.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(JFrameReporte.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            if(doc != null)
                doc.close();
            
            if(fos != null)
                try {
                    fos.close();
            } catch (IOException ex) {
                Logger.getLogger(JFrameReporte.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButReportePDFActionPerformed

