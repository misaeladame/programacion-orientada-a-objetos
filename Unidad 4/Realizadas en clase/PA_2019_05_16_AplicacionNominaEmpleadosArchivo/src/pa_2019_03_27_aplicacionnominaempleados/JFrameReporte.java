    public void ordenarAlfabetico()
    {
        for (int i=0; i<copia.length-1; i++)
            for(int j=i+1; j<copia.length; j++)
                if (copia[i].getNombre().compareTo(copia[j].getNombre())>0)
                {
                    Empleado aux = copia[i];
                    copia[i] = copia[j];
                    copia[j] = aux;
                }
    }
    
