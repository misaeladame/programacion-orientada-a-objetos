    private void mostrar() 
    {
        double total = 0;
        
        for(int r=0; r<datos.length; r++)
        {
            jTableExperimento.setValueAt(datos[r].getMasa(), r, 0);
            jTableExperimento.setValueAt(datos[r].getAceleracion(), r, 1);
            jTableExperimento.setValueAt(datos[r].fuerza(), r, 2);
            
            total += datos[r].fuerza();
        }
        jLabelTotal.setText(total+"");
    }

