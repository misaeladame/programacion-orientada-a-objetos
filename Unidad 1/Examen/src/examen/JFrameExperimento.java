    private void jButAjustarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAjustarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel)jTableExperimento.getModel();
        modelo.setRowCount(Integer.parseInt(jTextNoCuerpos.getText()));
        this.datos = new Experimento[Integer.parseInt(jTextNoCuerpos.getText())];
        
        for(int r = 0; r<datos.length; r++) 
            datos[r] = new Experimento();
        mostrar();
    }//GEN-LAST:event_jButAjustarActionPerformed

    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Experimento x = new Experimento();
        
        int posicion = (int)jSpinPosicion.getValue()-1;
        
        x.setMasa(Double.parseDouble(jTextMasa.getText()));
        x.setAceleracion(Double.parseDouble(jTextAceleracion.getText()));
        
        datos[posicion] = x;
        
        mostrar();
    }//GEN-LAST:event_jButCalcularActionPerformed

    private void jButDescendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButDescendenteActionPerformed
        // TODO add your handling code here:
        for(int i = 0; i<datos.length-1; i++)
            for(int j=i+1; j<datos.length; j++)
                if(datos[i].fuerza() < datos[j].fuerza()) 
                {
                    Experimento aux = datos[i];
                    datos[i] = datos[j];
                    datos[j] = aux;
                }
        mostrar();
    }//GEN-LAST:event_jButDescendenteActionPerformed

