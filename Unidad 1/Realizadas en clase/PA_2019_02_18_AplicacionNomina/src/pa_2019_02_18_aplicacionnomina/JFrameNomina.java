    public void mostrar(){
        
        DecimalFormat d = new DecimalFormat("#,###,##0.00");
        double total = 0;
        
        for(int r=0; r<nomina.length; r++){
            jTableNomina.setValueAt(nomina[r].getNombre(), r, 0);
            jTableNomina.setValueAt(nomina[r].getDiasTrabajados(), r, 1);
            jTableNomina.setValueAt(d.format(nomina[r].getSueldoDiario()), r, 2);
            jTableNomina.setValueAt(nomina[r].getHorasExtras(), r, 3);
            jTableNomina.setValueAt(nomina[r].getPorcentajeImpuesto(), r, 4);
            
            
            jTableNomina.setValueAt(d.format(nomina[r].importeHorasExtra()), r, 5);
            jTableNomina.setValueAt(d.format(nomina[r].importeImpuesto()), r, 6);
            jTableNomina.setValueAt(d.format(nomina[r].sueldo()), r, 7);
            
            total+=nomina[r].sueldo();
        }
        
        jLabTotal.setText(d.format(total)+"");
        
    }

