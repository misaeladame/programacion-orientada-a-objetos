    private void jButMayMenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButMayMenActionPerformed
        // TODO add your handling code here:
        Empleado mayor = nomina[0];
        Empleado menor = nomina[0];
        for(int r=1; r<nomina.length; r++){
            if(nomina[r].sueldo()>mayor.sueldo())
                mayor = nomina[r];
            if(nomina[r].sueldo()<menor.sueldo())
                menor = nomina[r];
        }
        
        jLabMayor.setText(mayor.getNombre());
        jLabMenor.setText(menor.getNombre());
    }//GEN-LAST:event_jButMayMenActionPerformed

    private void jButSueldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButSueldoActionPerformed
        // TODO add your handling code here:
        for(int i =0; i<nomina.length-1; i++)
            for(int j=i+1; j<nomina.length; j++)
                if(nomina[i].sueldo()<nomina[j].sueldo()){
                    Empleado aux = nomina[i];
                    nomina[i] = nomina[j];
                    nomina[j] = aux;
                }
        mostrar();
    }//GEN-LAST:event_jButSueldoActionPerformed

    private void jButAlfabetoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAlfabetoActionPerformed
        // TODO add your handling code here:
        for(int i =0; i<nomina.length-1; i++)
            for(int j=i+1; j<nomina.length; j++)
                if(nomina[i].getNombre().compareTo(nomina[j].getNombre()) > 0){
                    Empleado aux = nomina[i];
                    nomina[i] = nomina[j];
                    nomina[j] = aux;
                }
        mostrar();
    }//GEN-LAST:event_jButAlfabetoActionPerformed

