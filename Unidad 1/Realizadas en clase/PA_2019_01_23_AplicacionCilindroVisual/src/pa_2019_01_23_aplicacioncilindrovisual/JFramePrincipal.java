    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        // TODO add your handling code here:
        // declaracion de variables
        double radio, altura, area, volumen;
        
        // Entrada de datos
        radio = Double.parseDouble (jTextRadio.getText());
        altura = Double.parseDouble(jTextAltura.getText());
        
        //proceso
        volumen = Math.PI * Math.pow(radio, 2) * altura;
        area = 2 * Math.PI * radio * (radio + altura);
                
        // salida
        jLabArea.setText(area +"");
        jLabVolumen.setText(volumen +"");
        
                
        jLabArea.setBackground(jButtonFunciones.getBackground());
        jLabVolumen.setBackground(jButtonFunciones.getBackground());

    }//GEN-LAST:event_jButtonCalcularActionPerformed

